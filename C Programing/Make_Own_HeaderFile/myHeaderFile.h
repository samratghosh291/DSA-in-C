

//addition function

int add(int a, int b)
{
    return a+b;
}

//subtraction function

int sub(int a, int b)
{
    return a-b;
}

//multiplication function

int mul(int a, int b)
{
    return a*b;
}

//division function 

int div(int a, int b)
{
    return a/b;
}

//modulus function

int mod(int a, int b)
{
    return a%b;
}